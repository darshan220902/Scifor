# What are Resilient Distributed Datasets (RDDs) in Spark?

'''Resilient Distributed Datasets (RDDs) are a fundamental data 
structure in Apache Spark. RDDs are immutable, distributed collections 
of data that can be processed in parallel across a cluster of machines. 
They serve as the core data abstraction in Spark and provide a resilient, 
fault-tolerant way to store and process data'''